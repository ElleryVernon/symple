/**
 * 한 턴이 끝난 뒤 supermemory 에 저장할 내용 구성.
 * 날짜를 본문에 포함해, 추출된 기억이 "언제" 일인지 시간 맥락을 갖게 한다
 * (회상 시 "지난주에 말씀하셨던…" 같은 자연스러운 언급이 가능해진다).
 */
export function buildMemoryRecord(userText: string, assistantText: string): string {
  const date = new Date().toLocaleDateString("ko-KR", {
    timeZone: "Asia/Seoul",
    year: "numeric",
    month: "long",
    day: "numeric",
  });
  return `[${date} 상담]\n사용자: ${userText}\n상담사: ${assistantText}`;
}
